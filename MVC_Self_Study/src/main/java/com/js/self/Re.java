package com.js.self;

public class Re {
	
	private int x;
	private int y;
	private int r;
	private String c;
	
	public Re() {}

	public Re(int x, int y, int r, String c) {
		super();
		this.x = x;
		this.y = y;
		this.r = r;
		this.c = c;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getR() {
		return r;
	}

	public void setR(int r) {
		this.r = r;
	}

	public String getC() {
		return c;
	}

	public void setC(String c) {
		this.c = c;
	}
	
}
